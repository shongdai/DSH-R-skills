# 依赖:
#   - R版本: 4.5.2
#   - 主要包: tidyverse, ggplot2, clusterProfiler, enrichplot
#
# 输入文件:
#   - enrich.tsv: 富集分析结果（必需列：category, geneID, pvalue, count）
#   - gene_fc.tsv: 基因差异倍数（可选，用于着色）
#
# 输出文件:
#   - cnetplot.pdf: PDF 格式 cnet 图，适合论文出版
#   - cnetplot.png: PNG 格式 cnet 图，适合网页/PPT 展示
#
# 使用方法:
#   - R控制台: source("cnetplot_demo.R")
#   - 终端:    Rscript cnetplot_demo.R
