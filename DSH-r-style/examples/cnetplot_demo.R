# =============================================================================
# 0. R 包安装和载入
# =============================================================================

source("install_dependencies.R")           # 安装和加载依赖包

suppressPackageStartupMessages({           # 静默加载，仅显示关键日志
  library(tidyverse)                       # 数据处理和管道
  library(ggplot2)                         # 图形绘制
})


# =============================================================================
# 1. 常量配置
# =============================================================================

cat("INFO: 读取配置参数\n")                # 日志：开始配置

plot_width  <- 10                          # 图片输出宽度（英寸）
plot_height <- 8                           # 图片输出高度（英寸）
top_n       <- 10                          # 展示前 N 个通路


# =============================================================================
# 2. 数据读取与预处理
# =============================================================================

cat("INFO: 读取输入数据\n")                # 日志：开始读数据

data <- read_tsv(                          # 读取 TSV 格式数据
  "enrich.tsv",                            # 输入文件名
  show_col_types = FALSE                   # 不打印列类型
)

cat("SUCCESS: 已读取", nrow(data), "行数据\n")  # 日志：读取完毕


# =============================================================================
# 3. 绘图
# =============================================================================

cat("INFO: 绘制 cnet 图\n")                # 日志：开始绘图

p <- ggplot(data, aes(                     # 初始化 ggplot 对象
  x = reorder(Description, -pvalue),       # X 轴按 p 值排序
  y = Count,                               # Y 轴为基因计数
  fill = -log10(pvalue)                    # 颜色按 -log10(p) 映射
)) +
  geom_bar(stat = "identity") +            # 柱状图
  coord_flip() +                           # 翻转坐标轴
  theme_minimal()                          # 简洁主题


# =============================================================================
# 4. 图片输出
# =============================================================================

cat("INFO: 保存输出图片\n")                # 日志：开始输出

ggsave("output.pdf", p,                    # 保存 PDF
  width = plot_width, height = plot_height,
  device = cairo_pdf
)

ggsave("output.png", p,                    # 保存 PNG
  width = plot_width, height = plot_height,
  dpi = 300
)

cat("SUCCESS: 图形已保存\n")               # 日志：输出完毕
cat("  - output.pdf\n")                    # 列出输出文件
cat("  - output.png\n")
